    private void login(){
    
        try{
            
        
        String sql="select * from kullanici where kimlik=? and sifre=?";
        ps=con.prepareStatement(sql);
        ps.setString(1, txtid.getText());
        ps.setString(2, txtpass.getText());
        
        rs=ps.executeQuery();
        
        if(rs.next()){
            
          
            
             
             
            
             roles.role =  rs.getString("rolu");
            JOptionPane.showMessageDialog(this, "kimlik ve şıfre doğru ve "+rs.getString("rolu")+" olarak girdiniz");
            
          
              roles.pass=txtpass.getText();
              roles.id=rs.getString("kimlik");
              
              roles.logid=roles.id;
              
//              getUser();
              
            txtid.setText("");
            txtpass.setText("");
          this.setVisible(false);
       session();
     
        }
        
        else{
            JOptionPane.showMessageDialog(null,"Şıfre ya da kimlik hatalı");
        }
           
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }
    
    
 
    
